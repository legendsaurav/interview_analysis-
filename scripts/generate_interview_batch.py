# MAIN
# =========================

def main():
    parser = argparse.ArgumentParser()

    parser.add_argument("--qa-file", default="qa.txt")
    parser.add_argument("--sadtalker-dir", default="SadTalker")
    parser.add_argument("--source-image", default="face.png")
    # Removed --voice-key argument, API key is now hardcoded

    parser.add_argument("--output-root", default="generated")
    parser.add_argument("--voice-lang", default="en-us")
    parser.add_argument("--voice-codec", default="MP3")

    parser.add_argument("--cpu", action="store_true")
    parser.add_argument("--audio-only", action="store_true")
    parser.add_argument("--limit", type=int, default=0)

    args = parser.parse_args()

    qa_file = Path(args.qa_file)
    sadtalker_dir = Path(args.sadtalker_dir)
    source_image = Path(args.source_image)

    if not qa_file.exists():
        raise FileNotFoundError("QA file missing")

    questions = parse_questions(qa_file)

    if args.limit:
        questions = questions[:args.limit]

    run_dir = Path(args.output_root) / datetime.now().strftime("%Y%m%d_%H%M%S")
    audio_dir = run_dir / "audio"
    video_raw_dir = run_dir / "raw"
    final_video_dir = run_dir / "videos"

    audio_dir.mkdir(parents=True, exist_ok=True)
    video_raw_dir.mkdir(parents=True, exist_ok=True)
    final_video_dir.mkdir(parents=True, exist_ok=True)

    items = []
    for i, q in enumerate(questions, 1):
        items.append({
            "index": i,
            "qid": f"q{i:03d}",
            "question": q,
            "audio_path": (audio_dir / f"q{i:03d}.mp3").resolve(),
        })

    args_dict = {
        # "voice_key" removed, now hardcoded
        "voice_lang": args.voice_lang,
        "voice_codec": args.voice_codec,
        "audio_only": args.audio_only,
        "cpu": args.cpu,
        "no_still": False,
        "preprocess": "crop",
        "continue_on_video_error": True,
        "sadtalker_python": sys.executable,
        "sadtalker_dir": sadtalker_dir,
        "source_image": source_image,
        "video_raw_dir": video_raw_dir,
        "final_video_dir": final_video_dir,
        "source_audio_dir": None,
        "total": len(items),
    }


    # Phase 1: Generate all audio files
    print("\n=== Generating all audio files ===")
    audio_results = []
    with concurrent.futures.ProcessPoolExecutor() as executor:
        futures = [executor.submit(process_item, item, args_dict) for item in items]
        for f in concurrent.futures.as_completed(futures):
            res = f.result()
            if res:
                audio_results.append(res)

    print("All audio files generated. Waiting 1 minute before starting video generation...")
    time.sleep(60)

    # Phase 2: Generate all videos (limit concurrency to avoid resource exhaustion)
    print("\n=== Generating all videos ===")
    video_results = []
    with concurrent.futures.ProcessPoolExecutor(max_workers=3) as executor:
        futures = [executor.submit(process_video_item, item, args_dict) for item in audio_results]
        for f in concurrent.futures.as_completed(futures):
            res = f.result()
            if res:
                video_results.append(res)

    manifest = {
        "created_at": datetime.now().isoformat(),
        "items": video_results,
    }

    (run_dir / "manifest.json").write_text(json.dumps(manifest, indent=2))

    print("\nDONE ✅")
    print(f"Output folder: {run_dir}")


if __name__ == "__main__":
    main()