def parse_questions(file_path: Path) -> List[str]:
    questions = []
    for line in file_path.read_text(encoding="utf-8").splitlines():
        line = line.strip()
        if not line:
            continue
        if "Q:" in line:
            questions.append(line.split("Q:", 1)[1].strip())
    return questions


def synthesize_voicerss(text, output_file, lang="en-us", codec="MP3"):
    api_key = "759c79c9515242148848e58daaf0d74c"  # <-- Place your VoiceRSS API key here
    url = "https://api.voicerss.org/"
    params = {
        "key": api_key,
        "hl": lang,
        "src": text,
        "c": codec,
    }

    query = urllib.parse.urlencode(params)
    full_url = f"{url}?{query}"

    try:
        with urllib.request.urlopen(full_url) as response:
            data = response.read()
        if data.startswith(b'ERROR'):
            print(f"VoiceRSS error for text '{text}': {data.decode()}")
            return False
        with open(output_file, "wb") as f:
            f.write(data)
        return True
    except Exception as e:
        print(f"Failed to synthesize audio for '{text}': {e}")
        return False


def find_new_video(result_dir: Path, before_set):
    after = set(result_dir.glob("*.mp4"))
    new_files = list(after - before_set)
    if not new_files:
        raise RuntimeError("No video generated by SadTalker")
    return new_files[0]


def run_sadtalker(
    sadtalker_python,
    sadtalker_dir,
    source_image,
    audio_path,
    result_dir,
    use_cpu,
    use_still,
    preprocess,
):
    inference_py = sadtalker_dir / "inference.py"

    if not inference_py.exists():
        raise FileNotFoundError("SadTalker inference.py not found")

    result_dir.mkdir(parents=True, exist_ok=True)
    before = set(result_dir.glob("*.mp4"))

    cmd = [
        sadtalker_python,
        str(inference_py),
        "--driven_audio",
        str(audio_path),
        "--source_image",
        str(source_image),
        "--result_dir",
        str(result_dir),
        "--preprocess",
        preprocess,
    ]

    if use_still:
        cmd.append("--still")
    if use_cpu:
        cmd.append("--cpu")

    process = subprocess.run(cmd, cwd=sadtalker_dir)

    if process.returncode != 0:
        raise RuntimeError("SadTalker failed")

    return find_new_video(result_dir, before)


def build_sequence(items: List[dict], static_image: str):
    sequence = []
    for i, item in enumerate(items):
        sequence.append({
            "type": "video",
            "video": item["video_file"],
            "audio": item["audio_file"],
        })
        if i < len(items) - 1:
            sequence.append({"type": "image", "image": static_image})
    return sequence


def load_audio_only_items(audio_dir: Path, limit: int):
    files = sorted([p for p in audio_dir.glob("*") if p.suffix in [".mp3", ".wav"]])
    if limit > 0:
        files = files[:limit]

    items = []
    for i, f in enumerate(files, 1):
        items.append({
            "index": i,
            "qid": f"q{i:03d}",
            "question": f"Question {i}",
            "audio_path": f,
        })
    return items


# =========================
# WORKER FUNCTION
# =========================

def process_item(item, args_dict):
    idx = item["index"]
    qid = item["qid"]
    question = item["question"]
    audio_file = Path(item["audio_path"]).resolve()

    print(f"[{idx}/{args_dict['total']}] {question}")

    # Only generate audio in this phase
    print("Generating audio...")
    success = synthesize_voicerss(
        question,
        audio_file,
        args_dict["voice_lang"],
        args_dict["voice_codec"],
    )
    if not success:
        print(f"Skipping video for {qid} due to audio generation failure.")
        return None
    return {
        "index": idx,
        "qid": qid,
        "question": question,
        "audio_file": str(audio_file),
        "video_file": None,
    }

def process_video_item(item, args_dict):
    idx = item["index"]
    qid = item["qid"]
    question = item["question"]
    audio_file = Path(item["audio_file"]).resolve()

    video_file = None
    result_dir = (args_dict["video_raw_dir"] / qid).resolve()
    source_image = Path(args_dict["source_image"]).resolve()
    print(f"Running SadTalker with audio: {audio_file}")
    try:
        vid = run_sadtalker(
            args_dict["sadtalker_python"],
            args_dict["sadtalker_dir"],
            source_image,
            audio_file,
            result_dir,
            args_dict["cpu"],
            not args_dict["no_still"],
            args_dict["preprocess"],
        )

        final_video = args_dict["final_video_dir"] / f"video_{idx:03d}.mp4"
        shutil.copy2(vid, final_video)
        video_file = str(final_video)

    except Exception as e:
        if not args_dict["continue_on_video_error"]:
            raise
        print(f"Error: {e}")

    return {
        "index": idx,
        "question": question,
        "audio_file": str(audio_file),
        "video_file": video_file,
    }


